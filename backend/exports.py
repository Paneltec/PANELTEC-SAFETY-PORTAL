@router.post("/{eid}/render-pdf", status_code=201)
async def render_pdf_sibling(eid: str, user: dict = Depends(get_current_user)):
    """Phase 3.23 — On-demand PDF sibling generation for a non-PDF pack.

    Admin-only. Idempotent: if a PDF sibling already exists for the same
    (title, date_from, date_to, scope), returns that row instead of
    creating a duplicate. Used by the Audit Exports UI when a row is
    missing its PDF counterpart (transitional state after backfill failure)."""
    if user.get("role") != "admin":
        raise HTTPException(403, "Admin only")

    row = await db.audit_exports.find_one({"id": eid, "org_id": user["org_id"]}, {"_id": 0})
    if not row:
        raise HTTPException(404, "Not found")
    if row.get("format") == "pdf":
        raise HTTPException(400, "Row is already a PDF artefact")

    # Idempotency: if a sibling already exists in this group, return it.
    existing = await db.audit_exports.find_one({
        "org_id":    user["org_id"],
        "title":     row.get("title"),
        "date_from": row.get("date_from"),
        "date_to":   row.get("date_to"),
        "scope":     row.get("scope"),
        "format":    "pdf",
    }, {"_id": 0})
    if existing:
        return existing

    # Load bundle. JSON is the canonical format; CSV-zip falls back to
    # manifest-only (same trade-off as the backfill script).
    src_path = UPLOAD_DIR / (row.get("filename") or "")
    bundle: dict = {}
    if row.get("format") == "json" and src_path.exists():
        try:
            payload = json.loads(src_path.read_bytes())
            bundle = payload.get("data") if isinstance(payload, dict) else (payload or {})
            if not isinstance(bundle, dict):
                bundle = {}
        except Exception:
            bundle = {}

    meta = {
        "export_id":    row.get("id"),
        "title":        row.get("title") or "Compliance Evidence Pack",
        "date_from":    row.get("date_from"),
        "date_to":      row.get("date_to"),
        "scope":        row.get("scope") or "Org-wide",
        "generated_at": row.get("generated_at") or row.get("created_at"),
        "generated_by": row.get("generated_by") or "—",
        "counts":       row.get("counts") or {k: len(v) for k, v in bundle.items() if isinstance(v, list)},
        "sha256":       row.get("sha256") or "",
    }
    pdf_bytes = _pdf(bundle, meta)
    pdf_sha = hashlib.sha256(pdf_bytes).hexdigest()
    pdf_id = new_id()
    pdf_filename = f"{pdf_id}.pdf"
    (UPLOAD_DIR / pdf_filename).write_bytes(pdf_bytes)
    sibling = {
        "id":           pdf_id,
        "org_id":       user["org_id"],
        "workspace_id": row.get("workspace_id"),
        "title":        meta["title"],
        "date_from":    meta["date_from"],
        "date_to":      meta["date_to"],
        "include":      row.get("include") or list(bundle.keys()),
        "format":       "pdf",
        "file_url":     f"/api/files/exports/{pdf_filename}",
        "filename":     pdf_filename,
        "sha256":       pdf_sha,
        "size_bytes":   len(pdf_bytes),
        "counts":       meta["counts"],
        "scope":        meta["scope"],
        "generated_at": meta["generated_at"],
        "generated_by": meta["generated_by"],
        "created_at":   now_iso(),
        "sibling_of":   row.get("id"),
        "rendered_on_demand": True,
    }
    await db.audit_exports.insert_one(dict(sibling))
    sibling.pop("_id", None)
    return sibling
